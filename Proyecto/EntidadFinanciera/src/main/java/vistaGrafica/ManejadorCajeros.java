    private void llenarTabla(){
        for(Cajero caj:ControladorCajeros.arregloCajeros){
            rowData[0] = caj.getCedula();
            rowData[1] = caj.getIdCajero();
            rowData[2] = caj.getNombre();
            rowData[3] = caj.getApellido();
            rowData[4] = caj.getCorreo();
            rowData[5] = caj.getTelefono();
            rowData[6] = caj.getSueldo();
            rowData[7] = caj.getDireccion();
            
            model.addRow(rowData);
        }
    }
    
    
    private void vaciarTabla(){
        model.setRowCount(0);
    }
    
    private void actualizarTabla(){
        vaciarTabla();
        llenarTabla();
    }
    
    
