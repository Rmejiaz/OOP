    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        int row = jTableCajeros.getSelectedRow();
        
        int cedula = (int)jTableCajeros.getModel().getValueAt(row, 0);
        
        
        Cajero caj = new Cajero();
        caj.setCedula(cedula);
        
        
        // Tambi√©n hay que eliminarlo de la bd
        
        if(!contrCaj.borrar(caj))
            JOptionPane.showMessageDialog(null, "Error al eliminar cajero");
        else{
            JOptionPane.showMessageDialog(null, "El cajero con cedula "+cedula+" ha sido eliminado exitosamente");
            contrBD.eliminarCajero(caj);
            model.removeRow(row);
        }
    }//GEN-LAST:event_jButtonEliminarActionPerformed

    private void jButtonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificarActionPerformed
        int row = jTableCajeros.getSelectedRow();
        
        int cedula = (int)jTableCajeros.getModel().getValueAt(row, 0);
        
        
        Cajero caj = new Cajero();
        caj.setCedula(cedula);
        
        caj = (Cajero)contrCaj.consultarUno(caj);
        
        
        Cajero new_caj = new Cajero();
        
        // falta validar
        new_caj.setCedula(caj.getCedula());
        new_caj.setIdCajero(caj.getIdCajero());
        new_caj.setContrasena(caj.getContrasena());
        
        new_caj.setNombre(jTableCajeros.getModel().getValueAt(row, 2).toString());
        new_caj.setApellido(jTableCajeros.getModel().getValueAt(row, 3).toString());
        new_caj.setCorreo(jTableCajeros.getModel().getValueAt(row, 4).toString());
        new_caj.setTelefono(Long.parseLong(jTableCajeros.getModel().getValueAt(row, 5).toString()));
        new_caj.setSueldo(Double.parseDouble(jTableCajeros.getModel().getValueAt(row, 6).toString()));
        new_caj.setDireccion(jTableCajeros.getModel().getValueAt(row, 7).toString());
        
        
        if(!contrCaj.modificar(caj, new_caj))
            JOptionPane.showMessageDialog(null, "Error al modificar cajero");
        else
            contrBD.modificarCajero(new_caj);
    }//GEN-LAST:event_jButtonModificarActionPerformed

