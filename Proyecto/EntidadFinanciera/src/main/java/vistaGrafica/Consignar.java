    private void jButtonConsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsignarActionPerformed
        
        String pass = new String(jPasswordField.getPassword());
        
        if(jTextFieldIdCuenta.getText().equals("") || jTextFieldCedulaTitular.getText().equals("") || jTextFieldMonto.getText().equals("")){
                jLabelTotal.setText("Digite datos de cuenta");
                jLabelTotal.setForeground(Color.red);
                System.out.println(jTextFieldCedulaTitular.getText()+" , "+jTextFieldCedulaTitular.getText());
                JOptionPane.showMessageDialog(null, "Hay almenos un campo vac√≠o");
        }
        else{
            String numeroCuenta = jTextFieldIdCuenta.getText();
            int idTitular = Integer.parseInt(jTextFieldCedulaTitular.getText());
            float monto = Float.parseFloat(jTextFieldMonto.getText());

            if (monto <= 0){
                JOptionPane.showMessageDialog(null, "Ingrese un monto valido (no puede ser negativo)");
            }
            else{
            if(!pass.equals(caj.getContrasena())){
                JOptionPane.showMessageDialog(null, "Contrase√±a incorrecta");
            }
            else{
            if(jComboBoxTipoCuenta.getSelectedItem().equals("Ahorros"))
            {
                // Buscar la cuenta ahorros seleccionada:

                CuentaAhorros cuenta = new CuentaAhorros();
                cuenta.setIdCuentaAhorros(numeroCuenta);

                cuenta = (CuentaAhorros)contrAho.consultarUno(cuenta);

                if(cuenta.getIdCliente() != idTitular || cuenta.getSaldoCuentaAhorros() == 0.0f){
                    jLabelTotal.setText("Cuenta no encontrada");
                    jLabelTotal.setForeground(Color.red);
                    JOptionPane.showMessageDialog(null, "La cuenta digitada no existe");
                }
                else{
                    // Si la cuenta existe realiza la transacci√≥n


                        // Si los fondos alcanzan realiza la transacci√≥n:
                        
                        CuentaAhorros cuentaAux = new CuentaAhorros(cuenta);
                        cuentaAux.setSaldoCuentaAhorros(cuenta.getSaldoCuentaAhorros()+monto);
                        
                        contrAho.modificar(cuenta, cuentaAux);
                        contrBD.modificarCuentaAho(cuentaAux);
                        JOptionPane.showMessageDialog(null, "Transacci√≥n exitosa\n El nuevo saldo es: "+cuentaAux.getSaldoCuentaAhorros());
                    
                }
            }
            if(jComboBoxTipoCuenta.getSelectedItem().equals("Corriente")){
                // Buscar la cuenta corriente seleccionada:

                CuentaCorriente cuenta = new CuentaCorriente();
                cuenta.setIdCuentaCorriente(numeroCuenta);

                cuenta = (CuentaCorriente)contrCorr.consultarUno(cuenta);

                if(cuenta.getIdCliente() != idTitular || cuenta.getSaldoCuentaCorriente() == 0.0f){
                    jLabelTotal.setText("Cuenta no encontrada");
                    jLabelTotal.setForeground(Color.red);
                    JOptionPane.showMessageDialog(null, "La cuenta digitada no existe");
                }
                else{
                    // Si la cuenta existe realiza la transacci√≥n

                        // Si los fondos alcanzan realiza la transacci√≥n:
                        
                        CuentaCorriente cuentaAux = new CuentaCorriente(cuenta);
                        cuentaAux.setSaldoCuentaCorriente(cuenta.getSaldoCuentaCorriente()+monto);
                        
                        contrCorr.modificar(cuenta, cuentaAux);
                        contrBD.modificarCuentaCorr(cuentaAux);
                        JOptionPane.showMessageDialog(null, "Transacci√≥n exitosa\n El nuevo saldo es: "+cuentaAux.getSaldoCuentaCorriente());
                        
                        
                    
                }
            }
        }
            }
        }
    }//GEN-LAST:event_jButtonConsignarActionPerformed

